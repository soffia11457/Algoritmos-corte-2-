// ALMACENAMIENTO DE DATOS EN VARIABLES
#include <stdio.h>
int main()
{
int entero;
float flotante;
printf("Ingresa el valor entero: ");
scanf("%i", &entero);
printf("El valor ingresado es: %d\a", entero);
printf("Ingresa el valor float: ");
scanf("%f", &flotante);
printf("El valor ingresado es: %f\a", flotante);
return 0;
}
